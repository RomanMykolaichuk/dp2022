package oop1;

public class SuperClass1 {
	
	private String name;
	private int age;
	
	public SuperClass1() {
		this.name="SuperClass1";
	}
	
	public SuperClass1(String name) {
		this.name=name;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
	
	

}
